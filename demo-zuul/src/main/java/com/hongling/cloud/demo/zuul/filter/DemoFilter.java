package com.hongling.cloud.demo.zuul.filter;

import com.netflix.zuul.ZuulFilter;
import com.netflix.zuul.context.RequestContext;
import com.netflix.zuul.exception.ZuulException;
import org.springframework.stereotype.Component;

import javax.servlet.http.HttpServletRequest;
import java.io.IOException;

/**
 * TODO 过滤器
 *
 * @author wuzguo at 2018/9/17 11:20
 */

@Component
public class DemoFilter extends ZuulFilter {
    @Override
    public String filterType() {
        return "pre";
    }

    @Override
    public int filterOrder() {
        return 0;
    }

    @Override
    public boolean shouldFilter() {
        return true;
    }

    @Override
    public Object run() throws ZuulException {
        RequestContext context = RequestContext.getCurrentContext();
        HttpServletRequest request = context.getRequest();
        System.out.println(String.format(" %s， %s", request.getMethod(), request.getRequestURL().toString()));
        Object accessToken = request.getHeader("token");
        System.out.println("accessToken value: " + accessToken);

        if (accessToken == null) {
            System.out.println("token is empty");
            context.setSendZuulResponse(false);
            context.setResponseStatusCode(401);
            try {
                context.getResponse().getWriter().write("token is empty");
            } catch (IOException e) {
                e.printStackTrace();
            }
            return null;
        }

        System.out.println(" token is : " + accessToken);
        return accessToken;
    }
}
